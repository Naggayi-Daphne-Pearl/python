# Using the () operator
my_tuple = (1, 2, 3, 4)
print(my_tuple)  # Output: (1, 2, 3, 4)

# Using the tuple() function
my_tuple = tuple([1, 2, 3, 4])
print(my_tuple)  # Output: (1, 2, 3, 4)

# Access elements in a tuple 
print(my_tuple[0]) # Output: 1

print(type(my_tuple)) # Output:<class 'tuple'>

